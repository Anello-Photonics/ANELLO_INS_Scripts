def factory_config(mav_serialport, commands_list, timeout=1.0):
    """
    Sets parameters via MAVLink shell commands and prints the responses.

    mav_serialport: An object that supports .write(), .read(), and ._recv()
    commands_list: list of commands to run
    timeout: seconds to wait for each response
    """
    print("Applying factory mode configuration...")

    for cmd in commands_list:
        print(f"\n[Setting] {cmd}")

        # Wake shell
        mav_serialport.write("\n")
        time.sleep(0.1)

        # Send the command
        mav_serialport.write(cmd + "\n")
        time.sleep(0.1)

        # Capture response
        output = ""
        start_time = time.time()

        while time.time() - start_time < timeout:
            mav_serialport._recv()  # Pull in SERIAL_CONTROL messages
            chunk = mav_serialport.read(1024)
            if chunk:
                output += chunk
            time.sleep(0.05)

        output = output.strip()
        if output:
            print(output)
        else:
            print("[!] No response received.")

    print("\n[Done] Factory mode commands applied.\n")


